package com.Rahmat;

public class Vedtak {
    private int bredde;
    private int høyde;

    public Vedtak(int bredde, int høyde) {
        this.bredde = bredde;
        this.høyde = høyde;
    }

    public int getBredde() {
        return bredde;
    }

    public int getHøyde() {
        return høyde;
    }
}
